package com.ht.model.reportmodel;

public class EBillList {

	private String customsListNO;
	private String qty;
	private String decPrice;
	private String decTotal;
	private String curr;
	private String grossWt;
	private String netWt;

	public String getCustomsListNO() {
		return customsListNO;
	}

	public void setCustomsListNO(String customsListNO) {
		this.customsListNO = customsListNO;
	}

	public String getQty() {
		return qty;
	}

	public void setQty(String qty) {
		this.qty = qty;
	}

	public String getDecPrice() {
		return decPrice;
	}

	public void setDecPrice(String decPrice) {
		this.decPrice = decPrice;
	}

	public String getDecTotal() {
		return decTotal;
	}

	public void setDecTotal(String decTotal) {
		this.decTotal = decTotal;
	}

	public String getCurr() {
		return curr;
	}

	public void setCurr(String curr) {
		this.curr = curr;
	}

	public String getGrossWt() {
		return grossWt;
	}

	public void setGrossWt(String grossWt) {
		this.grossWt = grossWt;
	}

	public String getNetWt() {
		return netWt;
	}

	public void setNetWt(String netWt) {
		this.netWt = netWt;
	}

}
