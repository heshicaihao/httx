package com.ht.model;

public class UIGoodsImport {
	private String gname;
	private String gmodel;
	private String copGNo;    
	private int userkey;
	private String transInstkey;
	public String getGname() {
		return gname;
	}
	public void setGname(String gname) {
		this.gname = gname;
	}
	public String getGmodel() {
		return gmodel;
	}
	public void setGmodel(String gmodel) {
		this.gmodel = gmodel;
	}
	public String getCopGNo() {
		return copGNo;
	}
	public void setCopGNo(String copGNo) {
		this.copGNo = copGNo;
	}
	public int getUserkey() {
		return userkey;
	}
	public void setUserkey(int userkey) {
		this.userkey = userkey;
	}
	public String getTransInstkey() {
		return transInstkey;
	}
	public void setTransInstkey(String transInstkey) {
		this.transInstkey = transInstkey;
	}
}
